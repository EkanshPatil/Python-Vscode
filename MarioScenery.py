#coins

def coin(x,y) :
  pen.up()
  pen.goto(x,y)
  pen.down()
  pen.fillcolor("gold")
  pen.begin_fill()
  pen.circle(17)
  pen.end_fill() 
  pen.up()
  pen.backward(5)
  pen.left(90)
  pen.forward(9)
  pen.down()
