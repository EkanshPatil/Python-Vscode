#lucky blocks
def lucky(x,y):
  pen.up()
  pen.goto(x,y)
  pen.fillcolor("goldenrod")
  pen.begin_fill()
  pen.down()
  for i in range(4):
    pen.forward(40)
    pen.right(90)
  pen.end_fill()
  pen.up()
  pen.forward(20)
  pen.right(90)
  pen.forward(30)
  pen.right(180)
  pen.down()
  pen.color("black")
  pen.write("?", None, "center", " 20pt bold")
